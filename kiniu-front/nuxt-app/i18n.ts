const dictionaries: Record<LocaleCode, Record<keyof typeof zh, string>> = {
  'zh-CN': zh,
  'en-US': en
}

export type I18nKey = keyof typeof zh
export type UiTranslator = (key: I18nKey, params?: Record<string, string | number>) => string
type LocaleRef = Ref<LocaleCode> | ComputedRef<LocaleCode>
type UiI18nContext = {
  locale: LocaleRef
  t: UiTranslator
}

const UI_I18N_KEY = Symbol('ui-i18n')

export function normalizeLocale(locale?: string): LocaleCode {
  return locale === 'en-US' ? 'en-US' : 'zh-CN'
}

export function translate(locale: LocaleCode, key: I18nKey, params: Record<string, string | number> = {}) {
  const template = dictionaries[locale]?.[key] ?? dictionaries['zh-CN'][key] ?? key
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(params[name] ?? `{${name}}`))
}

export function provideUiI18n(locale: LocaleRef) {
  const context: UiI18nContext = {
    locale,
    t: (key, params) => translate(locale.value, key, params)
  }
  provide(UI_I18N_KEY, context)
  return context
}

export function useUiI18n() {
  const context = inject<UiI18nContext>(UI_I18N_KEY)
  if (context) return context

  const locale = ref<LocaleCode>('zh-CN')
  return {
    locale,
    t: (key: I18nKey, params?: Record<string, string | number>) => translate(locale.value, key, params)
  }
}
